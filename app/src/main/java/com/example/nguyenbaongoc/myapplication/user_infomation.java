package com.example.nguyenbaongoc.myapplication;

public class user_infomation {
    private String username;
    private String firstName;
    private String lastName;
    private String email;
    private String phonenumber;
    private String adress;
    private String birthday;

    public user_infomation(String username, String firstName, String lastName, String email, String phonenumber, String adress, String birthday) {
        this.username = username;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.phonenumber = phonenumber;
        this.adress = adress;
        this.birthday = birthday;
    }
}
